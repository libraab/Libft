#include "libft.h"

void	ft_putnbr(int nb)
{
	size_t	x;

	if (nb < 0)
	{
		ft_putchar('-');
		x = -nb;
	}
	else
		x = nb;
	if (nb > 9)
	{
		ft_putnbr(x / 10);
		x %= 10;
	}
	ft_putchar(x + '0');
}

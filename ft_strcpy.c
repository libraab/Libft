#include "libft.h"
char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;

	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	while (dst[i] != '\0')
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
